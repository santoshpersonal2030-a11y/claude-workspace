// Returns the item with translations for `locale` merged over the canonical
// English fields. A no-op for English or untranslated entries.
export function localizeConsultation(item: Consultation, locale: Locale): Consultation {
  const tr = DATA[locale]?.[item.slug];
  if (!tr) return item;
  const merged: Consultation = { ...item };
  (Object.keys(tr) as (keyof ConsultationText)[]).forEach((k) => {
    const v = tr[k];
    if (v !== undefined) {
      (merged[k] as ConsultationText[typeof k]) = v;
    }
  });
  return merged;
}
