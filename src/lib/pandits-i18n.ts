// Returns the item with translations for `locale` merged over the canonical
// English fields. A no-op for English or untranslated entries.
export function localizePandit(item: Pandit, locale: Locale): Pandit {
  const tr = DATA[locale]?.[item.slug];
  if (!tr) return item;
  const merged: Pandit = { ...item };
  (Object.keys(tr) as (keyof PanditText)[]).forEach((k) => {
    const v = tr[k];
    if (v !== undefined) {
      (merged[k] as PanditText[typeof k]) = v;
    }
  });
  return merged;
}
