// Returns the lifeEvent with translations for `locale` merged over the
// canonical English fields. A no-op for English or untranslated entries.
export function localizeLifeEvent(event: LifeEvent, locale: Locale): LifeEvent {
  const tr = LIFE_EVENT_I18N[locale]?.[event.slug];
  if (!tr) return event;
  const merged: LifeEvent = { ...event };
  (Object.keys(tr) as (keyof LifeEventText)[]).forEach((k) => {
    const v = tr[k];
    if (v !== undefined) {
      (merged[k] as LifeEventText[typeof k]) = v;
    }
  });
  return merged;
}
