// Returns the item with translations for `locale` merged over the canonical
// English fields. A no-op for English or untranslated entries.
export function localizeTemplePuja(item: TemplePuja, locale: Locale): TemplePuja {
  const tr = DATA[locale]?.[item.slug];
  if (!tr) return item;
  const merged: TemplePuja = { ...item };
  (Object.keys(tr) as (keyof TemplePujaText)[]).forEach((k) => {
    const v = tr[k];
    if (v !== undefined) {
      (merged[k] as TemplePujaText[typeof k]) = v;
    }
  });
  return merged;
}
