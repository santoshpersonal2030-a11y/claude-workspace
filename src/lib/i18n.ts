// Lightweight, dependency-free i18n. A flat dictionary per locale and a pure
// t() lookup with {var} interpolation and English fallback. Locale is persisted
// client-side (cookie) so static/ISR pages keep rendering without forcing
// dynamic server rendering.

export const LOCALES = ["en", "hi", "te"] as const;
export type Locale = (typeof LOCALES)[number];
export const DEFAULT_LOCALE: Locale = "en";

export const LOCALE_LABEL: Record<Locale, string> = {
  en: "English",
  hi: "हिन्दी",
  te: "తెలుగు",
};

export function isLocale(value: string | undefined | null): value is Locale {
  return (LOCALES as readonly string[]).includes(value ?? "");
}

// The locale currently being rendered, published by LanguageProvider. Lets
// dependency-free, non-React modules (e.g. the cart store's screen-reader
// announcements) localize without threading the locale through every call.
let activeLocale: Locale = DEFAULT_LOCALE;
export function setActiveLocale(locale: Locale): void {
  activeLocale = locale;
}
export function getActiveLocale(): Locale {
  return activeLocale;
}

